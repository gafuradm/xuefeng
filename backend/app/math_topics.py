# Удобная функция для получения плоского списка всех тем (без группировки по модулям)
def get_all_topics_flat():
    topics = []
    for category, levels in ALL_MATH_TOPICS.items():
        if isinstance(levels, dict):
            for level, topic_list in levels.items():
                for topic in topic_list:
                    topics.append({
                        "category": category,
                        "level": level,
                        "name": topic,
                        "id": f"{category}_{level}_{topic[:30]}".replace(" ", "_")
                    })
        else:
            # На случай если формат изменится
            for topic in levels:
                topics.append({
                    "category": category,
                    "level": "Общий",
                    "name": topic,
                    "id": f"{category}_Общий_{topic[:30]}".replace(" ", "_")
                })
    return topics

# Для построения плана группируем темы по модулям (например, по категориям и уровням)
def get_modules():
    modules = []
    for category, levels in ALL_MATH_TOPICS.items():
        if isinstance(levels, dict):
            for level, topic_list in levels.items():
                modules.append({
                    "name": f"{category} ({level})",
                    "topics": topic_list,
                    "category": category,
                    "level": level
                })
        else:
            modules.append({
                "name": f"{category}",
                "topics": levels,
                "category": category,
                "level": "Общий"
            })
    return modules

# Подсчёт общего количества тем
def get_total_topics_count():
    count = 0
    for category, levels in ALL_MATH_TOPICS.items():
        if isinstance(levels, dict):
            for level, topic_list in levels.items():
                count += len(topic_list)
        else:
            count += len(levels)
    return count

if __name__ == "__main__":
    print(f"📚 Всего тем в базе: {get_total_topics_count()}")
    print("\n📋 Список всех модулей:")
    for module in get_modules():
        print(f"  - {module['name']}: {len(module['topics'])} тем")