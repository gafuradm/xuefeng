def get_modules_for_subject(subject: str):
    """Возвращает список модулей для указанного предмета"""
    subject_key = subject.lower()
    if subject_key in SUBJECT_TOPICS:
        return SUBJECT_TOPICS[subject_key]["modules"]
    else:
        # Если предмет не найден, возвращаем математику по умолчанию
        return SUBJECT_TOPICS["математика"]["modules"]

def get_all_subjects():
    """Возвращает список всех доступных предметов"""
    return list(SUBJECT_TOPICS.keys())